while True:
    num = int(input("Digite um número: "))

    if num < 0:
        print("Número negativo não é aceito.")
        break

    print("Número digitado:", num)








#Utilize a estrutura de repetição 
# "while" para desenvolver um algoritmo,
#  em Python, que permita que um usuário
#  seja capaz de inserir apenas números
#  não-negativos. Caso o usuário digite um 
# valor negativo, o algoritmo deverá 
# interromper esse laço de repetição.